-- 1. ROLE
CREATE TABLE ROLE (
    MaRole NUMBER PRIMARY KEY,
    TenRole VARCHAR2(50) NOT NULL
);

-- 2. USERS
CREATE TABLE USERS (
    MaUser NUMBER PRIMARY KEY,
    Username VARCHAR2(50) UNIQUE NOT NULL,
    Password VARCHAR2(255) NOT NULL,
    MaRole NUMBER
);

-- 3. KHACHHANG
CREATE TABLE KHACHHANG (
    MaKH NUMBER PRIMARY KEY,
    HoTen VARCHAR2(100) NOT NULL,
    SDT VARCHAR2(15),
    Email VARCHAR2(100),
    DiaChi VARCHAR2(200)
);

-- 4. GAME
CREATE TABLE GAME (
    MaGame NUMBER PRIMARY KEY,
    TenGame VARCHAR2(200) NOT NULL,
    TheLoai VARCHAR2(50),
    NenTang VARCHAR2(50),
    GiaBan NUMBER(15,2),
    GiaThueNgay NUMBER(15,2)
);

-- 5. SANPHAM
CREATE TABLE SANPHAM (
    MaSP NUMBER PRIMARY KEY,
    MaGame NUMBER,
    GiaBan NUMBER(15,2)
);

-- 6. CD
CREATE TABLE CD (
    MaSP NUMBER PRIMARY KEY,
    TinhTrang VARCHAR2(50)
);

-- 7. ROM
CREATE TABLE ROM (
    MaSP NUMBER PRIMARY KEY,
    DungLuong VARCHAR2(20),
    LinkLuuTru VARCHAR2(500),
    SoLuotBan NUMBER DEFAULT 0
);

-- 8. HOADON
CREATE TABLE HOADON (
    MaHD NUMBER PRIMARY KEY,
    MaKH NUMBER,
    NgayLap TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    TongTien NUMBER(15,2) DEFAULT 0
);

-- 9. CTHOADON
CREATE TABLE CTHOADON (
    MaHD NUMBER,
    MaSP NUMBER,
    SoLuong NUMBER,
    DonGia NUMBER(15,2),
    PRIMARY KEY (MaHD, MaSP)
);

-- 10. PHIEUTHUE
CREATE TABLE PHIEUTHUE (
    MaPT NUMBER PRIMARY KEY,
    MaKH NUMBER,
    NgayThue TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    NgayTraDuKien TIMESTAMP,
    NgayTraThucTe TIMESTAMP,
    TienCoc NUMBER(15,2),
    TienPhat NUMBER(15,2) DEFAULT 0
);

-- 11. CTPHIEUTHUE
CREATE TABLE CTPHIEUTHUE (
    MaPT NUMBER,
    MaSP NUMBER,
    PRIMARY KEY (MaPT, MaSP)
);
--Các ràng buộc quan hệ 
-- Khóa ngoại cho USERS
ALTER TABLE USERS ADD CONSTRAINT FK_USERS_ROLE FOREIGN KEY (MaRole) REFERENCES ROLE(MaRole);

-- Khóa ngoại cho SANPHAM
ALTER TABLE SANPHAM ADD CONSTRAINT FK_SP_GAME FOREIGN KEY (MaGame) REFERENCES GAME(MaGame);

-- Khóa ngoại cho CD (Sản phẩm là CD)
ALTER TABLE CD ADD CONSTRAINT FK_CD_SP FOREIGN KEY (MaSP) REFERENCES SANPHAM(MaSP);

-- Khóa ngoại cho ROM (Sản phẩm là ROM)
ALTER TABLE ROM ADD CONSTRAINT FK_ROM_SP FOREIGN KEY (MaSP) REFERENCES SANPHAM(MaSP);

-- Khóa ngoại cho HOADON
ALTER TABLE HOADON ADD CONSTRAINT FK_HD_KH FOREIGN KEY (MaKH) REFERENCES KHACHHANG(MaKH);

-- Khóa ngoại cho CTHOADON
ALTER TABLE CTHOADON ADD CONSTRAINT FK_CTHD_HD FOREIGN KEY (MaHD) REFERENCES HOADON(MaHD);
ALTER TABLE CTHOADON ADD CONSTRAINT FK_CTHD_SP FOREIGN KEY (MaSP) REFERENCES SANPHAM(MaSP);

-- Khóa ngoại cho PHIEUTHUE
ALTER TABLE PHIEUTHUE ADD CONSTRAINT FK_PT_KH FOREIGN KEY (MaKH) REFERENCES KHACHHANG(MaKH);

-- Khóa ngoại cho CTPHIEUTHUE
ALTER TABLE CTPHIEUTHUE ADD CONSTRAINT FK_CTPT_PT FOREIGN KEY (MaPT) REFERENCES PHIEUTHUE(MaPT);
ALTER TABLE CTPHIEUTHUE ADD CONSTRAINT FK_CTPT_SP FOREIGN KEY (MaSP) REFERENCES SANPHAM(MaSP);
